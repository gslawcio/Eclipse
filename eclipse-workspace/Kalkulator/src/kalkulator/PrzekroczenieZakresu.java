package kalkulator;

public class PrzekroczenieZakresu extends WyjatekKalkulator {

	public PrzekroczenieZakresu() {
		
	}

	public PrzekroczenieZakresu(String message) {
		super(message);
	}

	public PrzekroczenieZakresu(String message, Throwable cause) {
		super(message, cause);

	}

	public PrzekroczenieZakresu(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public PrzekroczenieZakresu(Throwable cause) {
		super(cause);
		
	}

}
